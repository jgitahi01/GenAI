#operators 
# + - * / % ** //
num1 = 15
num2 = 4
#Addition
sum = num1 + num2 
print("The sum of 15 and 4 is", num1 + num2)
#Subtraction
difference = num1 - num2
print("The difference between 15 and 4 is", num1 - num2)
#Multiplication
product = num1 * num2
print("The product of 15 and 4 is", num1 * num2)
#Division
division = num1 / num2
print("The division of 15 and 4 is", num1 / num2)
#Modulus
modulus = num1 % num2
print("The modulus of 15 and 4 is", num1 % num2)
#Exponent
exponent = num1 ** num2
print("The exponent of 15 and 4 is", num1 ** num2)
#Floor Division
floor_division = num1 // num2
print("The floor division of 15 and 4 is", num1 // num2)
