#list of my favorite fruits
fruits = ['apple', 'banana', 'cherry', 'orange', 'kiwi', 'mango']
#printing the list of fruits
print("Original list:", fruits)
#append a new fruit
fruits.append('grape')
#printing the list of fruits after appending a new fruit
print("After adding a fruit:", fruits)
#removing a fruit from the list
fruits.remove('banana')
#printing the list of fruits after removing a fruit
print("After removing a fruit:", fruits)
#reversing the list of fruits
fruits.reverse()
#printing the list of fruits after reversing the list
print("Reversed list:", fruits)